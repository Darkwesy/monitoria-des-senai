# Slice
string = "Hello World"
string2 = "Cortar String"
print(string[0:6]) # Resultado esperado -> Hello
print(string[6:]) # Resultado esperado -> World
# A posição do : indica se ele ira cortar antes ou depois do caractere especificado.
print(string2[12:]) # Resultado esperado -> g
print(string2[:1]) # Resultado esperado -> C