# Inicialização de uma matriz vazia
matriz = [[],[],[]]
print(matriz) # Resultado esperado -> [[], [], []]
# Inicialização de uma matriz com valores fixo
matrizPreenchida = [
  ["String"], # Lista 0
  [4.5], # Lista 1
  [True], # Lista 2
  [5] # Lista 3
];
print(matrizPreenchida[0]) # Resultado Esperado -> String
print(matrizPreenchida[1]) # Resultado Espesrado -> 4.5
print(matrizPreenchida[2]) # Resultado Esperado -> True
print(matrizPreenchida[3]) # Resultado Esperado -> 5